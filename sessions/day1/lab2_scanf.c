// Goal is to get familiar with scanf() and user input in C 
#include <stdio.h>

int main()
{	int x,y;
		scanf("%d",&x);
		scanf("%d",&y);
		printf("\n x+y = %d",x+y);
		printf("\n x*y = %d",x*y);
		printf("\n x/y = %d",x/y);
		printf("\n x-y = %d",x-y);
		printf("\n x%%y = %d",x%y);

		
		return 0;
}
